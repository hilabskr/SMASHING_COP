제7장 기타 
제21조(회원에 대한 통지) 
1. ‘회사’가 ‘회원’에 대한 통지를 하는 경우 본 약관에 별도 규정이 없는 한 ‘서비스’ 내 공지, 알림 메시지 등으로 할 수 있습니다.
