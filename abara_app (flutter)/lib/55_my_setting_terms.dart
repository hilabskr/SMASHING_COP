(12) 정당한 사유 없이 회원 가입, 탈퇴를 수차례 반복하여 금전적 이득을 취하는 행위
(13) ‘회사’의 '서비스' 운영에 지장을 초래하는 행위
(14) '서비스' 운영에 지장을 주거나 줄 우려가 있는 일체의 행위, 기타 관계 법령에 위배되는 행위
본 항에 따른 즉시 계약 해지 시, ‘서비스’ 이용을 통해 획득한 혜택 등도 모두 소멸하며 ‘회사’는 이에 대해 별도의 보상을 하지 않습니다.
