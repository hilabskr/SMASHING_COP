제22조(손해배상) 
1. 회사가 제공하는 '서비스'로 ‘회원’에게 손해가 발생한 경우에 이러한 손해가 회사의 고의나 중과실을 근거해 발생한 경우에 한하여 ‘회사’에서 책임을 부담하며, 그 책임의 범위는 통상 손해에 한합니다.
2. ‘회원’의 고의나 과실에 의해 본 약관의 규정을 위반함으로써 ‘회사’에 손해가 발생하는 경우, 이 약관을 위반한 회원은 ‘회사’에 발생하는 모든 손해를 배상해야 합니다. 
3. ‘회원’이 '서비스'를 이용하면서 행한 불법행위나 ‘회원’의 고의, 과실에 의한 약관 위반 행위가 회사에 회원 이외의 제3자로부터 손해배상청구 또는 소송을 비롯한 각종 이의제기를 받게 되는 경우 ‘회원’은 ‘회사’에 발생한 손해를 배상해야 합니다.
제23조(면책사항) 
