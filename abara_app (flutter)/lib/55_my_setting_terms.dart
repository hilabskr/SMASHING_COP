(5) 범죄와 결부된다고 객관적으로 인정되는 내용일 경우
(6) 다른 ‘회원’ 또는 제 3자의 저작권 등 기타 권리를 침해하는 내용인 경우
(7) 회사에서 규정한 '게시물' 원칙에 어긋나거나, '서비스' 성격에 부합하지 않는 경우
(8) 특정업체 및 특정 상품에 대한 근거 없는 비방 및 가격 비교 등의 영업방해 행위에 해당하는 경우
(9) 기타 관계 법령에 위배된다고 판단되는 경우 
제4장 계약 해지 및 이용 제한 
제15조(계약 해지 및 이용 제한) 
